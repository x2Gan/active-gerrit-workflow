def ensure_review_input_object(data: Any) -> Mapping[str, Any]:
    if not isinstance(data, Mapping):
        raise CLIUsageError("Review input must be a JSON object.")
    allowed = {"message", "labels", "comments", "tag", "drafts", "notify"}
    unknown = sorted(str(key) for key in data.keys() if key not in allowed)
    if unknown:
        raise CLIUsageError(f"Review input contains unsupported field(s): {', '.join(unknown)}.")
    if not any(key in data for key in ("message", "labels", "comments", "drafts")):
        raise CLIUsageError("Review input must include at least one of message, labels, comments, or drafts.")
    return data


def validate_message(message: Any) -> str:
    if not isinstance(message, str) or not message.strip():
        raise CLIUsageError("Review message must be a non-empty string.")
    return message


def validate_tag(tag: Any) -> str:
    if tag is None:
        return DEFAULT_REVIEW_TAG
    if not isinstance(tag, str) or not tag.strip():
        raise CLIUsageError("Review tag must be a non-empty string.")
    return tag


def validate_notify(notify: Any) -> str:
    value = DEFAULT_NOTIFY if notify is None else notify
    if value not in NOTIFY_OPTIONS:
        allowed = ", ".join(NOTIFY_OPTIONS)
        raise CLIUsageError(f"notify must be one of: {allowed}.")
    return str(value)


def validate_drafts(drafts: Any) -> Optional[str]:
    if drafts is None:
        return None
    if drafts not in DRAFTS_OPTIONS:
        allowed = ", ".join(DRAFTS_OPTIONS)
        raise CLIUsageError(f"drafts must be one of: {allowed}.")
    return str(drafts)


def validate_label_value(label: str, value: Any, detail: Mapping[str, Any]) -> int:
    if not label or not label.strip():
        raise CLIUsageError("Label names must be non-empty strings.")
    if isinstance(value, bool) or not isinstance(value, int):
        raise CLIUsageError(f"Label {label} value must be an integer.")

    labels = detail.get("labels") or {}
    if not isinstance(labels, Mapping) or label not in labels:
        raise CLIUsageError(f"Label {label} is not available on this change.")

    label_info = labels.get(label)
    if isinstance(label_info, Mapping):
        values = label_info.get("values")
        if isinstance(values, Mapping) and str(value) not in {str(key) for key in values.keys()}:
            allowed = ", ".join(str(key) for key in values.keys())
            raise CLIUsageError(f"Label {label} value {value} is outside allowed values: {allowed}.")

    permitted_labels = detail.get("permitted_labels")
    if isinstance(permitted_labels, Mapping):
        permitted_values = permitted_labels.get(label)
        if isinstance(permitted_values, Sequence) and not isinstance(permitted_values, (str, bytes, bytearray)):
            normalized = {int(item) for item in permitted_values if isinstance(item, int) and not isinstance(item, bool)}
            if normalized and value not in normalized:
                allowed = ", ".join(str(item) for item in sorted(normalized))
                raise CLIUsageError(f"Label {label} value {value} is not permitted for the current user: {allowed}.")
    return value


def validate_labels(labels: Any, detail: Mapping[str, Any]) -> Dict[str, int]:
    if labels is None:
        return {}
    if not isinstance(labels, Mapping):
        raise CLIUsageError("Review labels must be a JSON object.")
    result = {}
    for label, value in labels.items():
        if not isinstance(label, str):
            raise CLIUsageError("Label names must be strings.")
        result[label] = validate_label_value(label, value, detail)
    return result


def validate_range(range_info: Any) -> Mapping[str, int]:
    if not isinstance(range_info, Mapping):
        raise CLIUsageError("Comment range must be a JSON object.")
    required = ("start_line", "start_character", "end_line", "end_character")
    missing = [key for key in required if key not in range_info]
    if missing:
        raise CLIUsageError(f"Comment range missing field(s): {', '.join(missing)}.")
    normalized = {}
    for key in required:
        value = range_info[key]
        if isinstance(value, bool) or not isinstance(value, int):
            raise CLIUsageError(f"Comment range {key} must be an integer.")
        if "line" in key and value <= 0:
            raise CLIUsageError(f"Comment range {key} must be greater than zero.")
        if "character" in key and value < 0:
            raise CLIUsageError(f"Comment range {key} must be greater than or equal to zero.")
        normalized[key] = value
    if (normalized["end_line"], normalized["end_character"]) < (
        normalized["start_line"],
        normalized["start_character"],
    ):
        raise CLIUsageError("Comment range end must be after start.")
    return normalized


def validate_comment_input(file_path: str, comment: Any) -> Dict[str, Any]:
    if not isinstance(comment, Mapping):
        raise CLIUsageError(f"Comment for {file_path} must be a JSON object.")
    allowed = {"message", "unresolved", "line", "range", "side", "parent", "in_reply_to"}
    unknown = sorted(str(key) for key in comment.keys() if key not in allowed)
    if unknown:
        raise CLIUsageError(f"Comment for {file_path} contains unsupported field(s): {', '.join(unknown)}.")
    if "message" not in comment:
        raise CLIUsageError(f"Comment for {file_path} must include message.")
    if not isinstance(comment.get("message"), str) or not comment.get("message", "").strip():
        raise CLIUsageError(f"Comment for {file_path} message must be a non-empty string.")
    if "unresolved" not in comment or not isinstance(comment.get("unresolved"), bool):
        raise CLIUsageError(f"Comment for {file_path} must include unresolved as a boolean.")

    normalized = {
        "message": comment["message"],
        "unresolved": comment["unresolved"],
    }
    if "line" in comment:
        line = comment["line"]
        if isinstance(line, bool) or not isinstance(line, int) or line <= 0:
            raise CLIUsageError(f"Comment for {file_path} line must be a positive integer.")
        normalized["line"] = line
    if "range" in comment:
        normalized["range"] = dict(validate_range(comment["range"]))
    if file_path == "/PATCHSET_LEVEL" and ("line" in normalized or "range" in normalized):
        raise CLIUsageError("/PATCHSET_LEVEL comments must not include line or range.")
    for key in ("side", "parent", "in_reply_to"):
        if key in comment:
            normalized[key] = comment[key]
    return normalized


def validate_comments(comments: Any, files: set) -> Dict[str, Sequence[Dict[str, Any]]]:
    if comments is None:
        return {}
    if not isinstance(comments, Mapping):
        raise CLIUsageError("Review comments must be a JSON object grouped by file path.")
    result = {}
    for file_path, path_comments in comments.items():
        if not isinstance(file_path, str) or not file_path.strip():
            raise CLIUsageError("Comment file paths must be non-empty strings.")
        if file_path != "/PATCHSET_LEVEL" and file_path not in files:
            raise CLIUsageError(f"Comment file path {file_path} is not present in the selected revision.")
        if not isinstance(path_comments, Sequence) or isinstance(path_comments, (str, bytes, bytearray)):
            raise CLIUsageError(f"Comments for {file_path} must be a JSON array.")
        result[file_path] = [validate_comment_input(file_path, comment) for comment in path_comments]
    return result


def count_comments(comments: Mapping[str, Sequence[Mapping[str, Any]]]) -> int:
    return sum(len(items) for items in comments.values())


def build_review_payload(
    raw_input: Mapping[str, Any],
    detail: Mapping[str, Any],
    files: set,
    notify_override: Optional[str],
) -> Dict[str, Any]:
    data = ensure_review_input_object(raw_input)
    payload: Dict[str, Any] = {
        "tag": validate_tag(data.get("tag")),
        "notify": validate_notify(notify_override or data.get("notify")),
    }
    if "message" in data:
        payload["message"] = validate_message(data.get("message"))
    labels = validate_labels(data.get("labels"), detail)
    if labels:
        payload["labels"] = labels
    comments = validate_comments(data.get("comments"), files)
    if comments:
        payload["comments"] = comments
    drafts = validate_drafts(data.get("drafts"))
    if drafts:
        payload["drafts"] = drafts
    return payload


