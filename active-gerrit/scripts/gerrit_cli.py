def content_encoding(content: Any, content_type: str) -> str:
    if not isinstance(content, str):
        return "json"
    if "text/plain" not in content_type.lower():
        return "text"
    try:
        base64.b64decode("".join(content.split()).encode("ascii"), validate=True)
    except Exception:
        return "text"
    return "base64"


