def resolve_revision_from_detail(change: Mapping[str, Any], requested_revision: str) -> Dict[str, Any]:
    requested = requested_revision.strip()
    if not requested:
        raise CLIUsageError("--revision must not be empty.")
    if requested != "current":
        return {
            "requested_revision": requested,
            "revision": requested,
            "revision_sha": None,
            "patch_set": None,
        }

    current_revision = change.get("current_revision")
    if not current_revision:
        raise GerritParseError("Gerrit change detail did not include current_revision.")
    patch_set = current_patch_set(change)
    resolved_revision = str(patch_set) if patch_set is not None else str(current_revision)
    return {
        "requested_revision": requested,
        "revision": resolved_revision,
        "revision_sha": current_revision,
        "patch_set": patch_set,
    }


